Создана клиентская база данных банка - упрощенная СRM система.
База данных содержит информацию о клиентах, сделках, продуктах и полученных доходах.
Использоание данной БД позваляет оценить:
	- доход от клиента,
	- эффективность сотрудников,
	- востребованность программ,
	- рентабельность филиалов / офисов,
	- продуктовую корзину банка.



